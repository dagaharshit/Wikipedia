package com.example.wikipedia;


public class Category {
	public String title;
	public int index;
	
	public Category()
	{
		
	}
	
	public Category(String t, int i)
	{
		title = t;
		index = i;
	}
	
	public void setTitle(String t)
	{
		title = t;
	}
	
	public void setIndex(int i)
	{
		index = i;
	}
	
	
}
